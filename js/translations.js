// Langue actuelle (par défaut français)
let currentLanguage = 'fr';

// Fonction pour changer la langue
function changeLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('codeurs-pro-lang', lang);
    updateContent();
    updateLanguageButton();
}

// Fonction pour mettre à jour le contenu
function updateContent() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[currentLanguage][key]) {
            element.textContent = translations[currentLanguage][key];
        }
    });

    // Mettre à jour les placeholders des formulaires
    updateFormPlaceholders();

    // Mettre à jour Typed.js
    updateTypedText();
}

// Fonction pour mettre à jour les placeholders
function updateFormPlaceholders() {
    const nameInput = document.getElementById('name');
    const emailInput = document.getElementById('email');
    const subjectInput = document.getElementById('subject');
    const messageInput = document.getElementById('message');

    if (nameInput) nameInput.placeholder = translations[currentLanguage].contact_form_name;
    if (emailInput) emailInput.placeholder = translations[currentLanguage].contact_form_email;
    if (subjectInput) subjectInput.placeholder = translations[currentLanguage].contact_form_subject;
    if (messageInput) messageInput.placeholder = translations[currentLanguage].contact_form_message;
}

// Fonction pour mettre à jour le texte Typed.js
function updateTypedText() {
    const typedStrings = currentLanguage === 'fr'
        ? [
            'Code\'s Our Professional',
            'Développement Web Moderne',
            'Applications Mobiles Performantes',
            'Solutions Digitales Innovantes',
            'Votre Partenaire Digital au Burkina Faso'
        ]
        : [
            'Code\'s Our Professional',
            'Modern Web Development',
            'High-Performance Mobile Apps',
            'Innovative Digital Solutions',
            'Your Digital Partner in Burkina Faso'
        ];

    // Recréer l'instance Typed avec les nouvelles chaînes
    if (window.typedInstance) {
        window.typedInstance.destroy();
    }

    window.typedInstance = new Typed('#typed-text', {
        strings: typedStrings,
        typeSpeed: 50,
        backSpeed: 30,
        backDelay: 2000,
        loop: true,
        showCursor: true,
        cursorChar: '|'
    });
}

// Fonction pour mettre à jour le bouton de langue
function updateLanguageButton() {
    const langBtn = document.getElementById('currentLang');
    if (langBtn) {
        langBtn.textContent = currentLanguage.toUpperCase();
    }
}

// Initialisation au chargement
document.addEventListener('DOMContentLoaded', function () {
    // Récupérer la langue sauvegardée
    const savedLang = localStorage.getItem('codeurs-pro-lang');
    if (savedLang) {
        currentLanguage = savedLang;
    }

    // Mettre à jour le contenu
    updateContent();
    updateLanguageButton();

    // Ajouter l'événement au bouton de langue
    const languageBtn = document.getElementById('languageBtn');
    if (languageBtn) {
        languageBtn.addEventListener('click', function () {
            const newLang = currentLanguage === 'fr' ? 'en' : 'fr';
            changeLanguage(newLang);
        });
    }
});

// Exporter pour utilisation globale
window.changeLanguage = changeLanguage;
window.currentLanguage = () => currentLanguage;
window.getTranslation = (key) => translations[currentLanguage][key] || key;